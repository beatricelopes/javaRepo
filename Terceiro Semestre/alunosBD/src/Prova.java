package src;

public class Prova {
	public double Nota;
	public double Peso;
	
	public Prova(double nota, double peso) {
		super();
		Nota = nota;
		Peso = peso;
	}

	public double getNota() {
		return Nota;
	}

	public void setNota(double nota) {
		Nota = nota;
	}

	public double getPeso() {
		return Peso;
	}

	public void setPeso(double peso) {
		Peso = peso;
	}
	
	
	
}
