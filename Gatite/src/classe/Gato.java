package classe;

/**
 *
 * @author beatr
 */
public class Gato {
    //atributos
    private char sexo;
    private float altura;
    private float peso;
    private String cor;
    private String nome;
    
    public Gato(char sexo, float altura, float peso, String cor, String nome) {
        this.sexo = sexo;
        this.altura = altura;
        this.peso = peso;
        this.cor = cor;
        this.nome = nome;
    }

    public char getSexo() {
        return sexo;
    }

    public void setSexo(char sexo) {
        this.sexo = sexo;
    }

    public float getAltura() {
        return altura;
    }

    public void setAltura(float altura) {
        this.altura = altura;
    }

    public float getPeso() {
        return peso;
    }

    public void setPeso(float peso) {
        this.peso = peso;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

}
